function mkdirpSync(dirName) {
    if (fs.existsSync(dirName)) {
        return true;
    } else {
        if (mkdirpSync(path.dirname(dirName))) {
            fs.mkdirSync(dirName);
            return true;
        }
    }
    return false
}

async function sleep(ms) {
    return new Promise((resolve => {
        setTimeout(()=>{
            resolve();
        }, ms)
    }));
}

